package model.classes;

/**
 *
 * @author Wylianne
 */
public class Instrumento {
    private String cor;
    private int ano;
    private String marca;

    public Instrumento(String cor, int ano, String marca) {
        this.cor = cor;
        this.ano = ano;
        this.marca = marca;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }
    
    
}
